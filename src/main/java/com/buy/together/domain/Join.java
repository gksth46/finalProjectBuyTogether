package com.buy.together.domain;

public class Join {

	private int join_number;
	private int user_numner;
	private int match_status_number;
	private int buyTogether_number;

	public Join() {

	}

	public int getJoin_number() {
		return join_number;
	}

	public void setJoin_number(int join_number) {
		this.join_number = join_number;
	}

	public int getUser_numner() {
		return user_numner;
	}

	public void setUser_numner(int user_numner) {
		this.user_numner = user_numner;
	}

	public int getMatch_status_number() {
		return match_status_number;
	}

	public void setMatch_status_number(int match_status_number) {
		this.match_status_number = match_status_number;
	}

	public int getBuyTogether_number() {
		return buyTogether_number;
	}

	public void setBuyTogether_number(int buyTogether_number) {
		this.buyTogether_number = buyTogether_number;
	}

}
