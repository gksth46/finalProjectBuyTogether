package com.buy.together.domain;

public class Search {

	private int keyword_number;
	private String keyword;

	public Search() {
	}

	public int getKeyword_number() {
		return keyword_number;
	}

	public void setKeyword_number(int keyword_number) {
		this.keyword_number = keyword_number;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

}
